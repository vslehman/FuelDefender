package os.milestone3;

public class GoogleTripTime {
	private long driveTime;
	private long bikeTime;
	private long walkTime;
	
	public long getDriveTime() {
		return driveTime;
	}
	public void setDriveTime(long driveTime) {
		this.driveTime = driveTime;
	}
	public long getBikeTime() {
		return bikeTime;
	}
	public void setBikeTime(long bikeTime) {
		this.bikeTime = bikeTime;
	}
	public long getWalkTime() {
		return walkTime;
	}
	public void setWalkTime(long walkTime) {
		this.walkTime = walkTime;
	}
}
